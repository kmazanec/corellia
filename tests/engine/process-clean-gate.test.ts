describe('PROCESS_CLEAN_PATTERNS', () => {
  it('is a non-empty readonly array', () => {
    expect(Array.isArray(PROCESS_CLEAN_PATTERNS)).toBe(true);
    expect(PROCESS_CLEAN_PATTERNS.length).toBeGreaterThan(0);
  });

  it('contains "tree/"', () => {
    expect(PROCESS_CLEAN_PATTERNS).toContain('tree/');
  });

  it('contains "improve-factory"', () => {
    expect(PROCESS_CLEAN_PATTERNS).toContain('improve-factory');
  });

  it('contains "goalid"', () => {
    expect(PROCESS_CLEAN_PATTERNS).toContain('goalid');
  });

  it('contains "corellia"', () => {
    expect(PROCESS_CLEAN_PATTERNS).toContain('corellia');
  });

  it('is deterministic (same array every import)', () => {
    // The exported constant must not change across calls — it's a module-level const.
    const first = [...PROCESS_CLEAN_PATTERNS];
    const second = [...PROCESS_CLEAN_PATTERNS];
    expect(first).toEqual(second);
  });
});
